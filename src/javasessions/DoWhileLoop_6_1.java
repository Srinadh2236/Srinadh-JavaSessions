package javasessions;

public class DoWhileLoop_6_1 {

	public static void main(String[] args) {
		
		int i =1;
		do {
			System.out.println(i);
			i++;
		}
		while(i<=10);
		
		//----------------
		int k=1;
		do {
			System.out.println(k);
			k++;
		}
		//--------------
		while(k<=10);
		
		int l =1;
		
		while(l<=10)
		{
			System.out.println(l);
			l++;
		}
	System.out.println("--------------");		
		int m=10;
		do {
			System.out.println(m);
			m--;
		}
		while(m>=0);
		
		
		
		

	}

}
